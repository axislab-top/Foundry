function Parse-EnvFile {
    param([string]$content)
    $variables = @{}
    $lines = $content -split "`r?`n"

    foreach ($line in $lines) {
        $trimmed = $line.Trim()
        if ([string]::IsNullOrWhiteSpace($trimmed) -or $trimmed.StartsWith("#")) {
            continue
        }
        if ($trimmed -match '^([^#=]+?)\s*=\s*(.*)$') {
            $key = $matches[1].Trim()
            $value = $matches[2].Trim()

            # Remove inline comment for unquoted values.
            if (-not ($value.StartsWith('"') -or $value.StartsWith("'"))) {
                $commentIndex = $value.IndexOf("#")
                if ($commentIndex -ge 0) {
                    $value = $value.Substring(0, $commentIndex).Trim()
                }
            }

            # Strip matching quotes.
            if ($value -match '^"(.*)"$') {
                $value = $matches[1]
            } elseif ($value -match "^'(.*)'$") {
                $value = $matches[1]
            }

            $variables[$key] = $value
        }
    }

    return $variables
}

function Ensure-Directory {
    param([string]$filePath)
    $dir = Split-Path $filePath -Parent
    if (-not [string]::IsNullOrWhiteSpace($dir) -and -not (Test-Path $dir)) {
        New-Item -ItemType Directory -Path $dir -Force | Out-Null
    }
}

function Generate-ServiceEnvFile {
    param(
        [hashtable]$variables,
        [string[]]$requiredVars,
        [string]$outputPath,
        [string]$serviceName
    )

    $localVars = @{}
    foreach ($k in $variables.Keys) {
        $localVars[$k] = $variables[$k]
    }

    $out = @()
    $out += "# $serviceName service env"
    $out += "# generated by scripts/env-manager.ps1"
    $out += "# generated at: $(Get-Date -Format 'yyyy-MM-dd HH:mm:ss')"
    $out += ""

    switch ($serviceName) {
        "gateway" {
            $out += "NODE_ENV=$($localVars['NODE_ENV'])"
            $out += "PORT=$($localVars['GATEWAY_SERVICE_PORT'])"
            $out += ""
            if ($localVars.ContainsKey('GATEWAY_DB_DATABASE')) { $localVars['DB_DATABASE'] = $localVars['GATEWAY_DB_DATABASE'] }
            if ($localVars.ContainsKey('REDIS_DB_GATEWAY')) { $localVars['REDIS_DB'] = $localVars['REDIS_DB_GATEWAY'] }
        }
        "api" {
            $out += "NODE_ENV=$($localVars['NODE_ENV'])"
            $out += "PORT=$($localVars['API_SERVICE_PORT'])"
            $out += ""
            if ($localVars.ContainsKey('API_DB_DATABASE')) { $localVars['DB_DATABASE'] = $localVars['API_DB_DATABASE'] }
            if ($localVars.ContainsKey('REDIS_DB_API')) { $localVars['REDIS_DB'] = $localVars['REDIS_DB_API'] }
            if (-not $localVars.ContainsKey('ENABLE_SESSION_MEMORY')) { $localVars['ENABLE_SESSION_MEMORY'] = 'true' }
            if (-not $localVars.ContainsKey('ENABLE_MEMORY_CONSOLIDATION')) { $localVars['ENABLE_MEMORY_CONSOLIDATION'] = 'false' }
            if (-not $localVars.ContainsKey('MEMORY_CONSOLIDATION_WINDOW_MESSAGES')) { $localVars['MEMORY_CONSOLIDATION_WINDOW_MESSAGES'] = '50' }
            if (-not $localVars.ContainsKey('MEMORY_RAG_QUERY_TIMEOUT_MS')) { $localVars['MEMORY_RAG_QUERY_TIMEOUT_MS'] = '280' }
            if (-not $localVars.ContainsKey('MEMORY_HYBRID_VECTOR_WEIGHT')) { $localVars['MEMORY_HYBRID_VECTOR_WEIGHT'] = '0.72' }
        }
        "webhooks" {
            $out += "NODE_ENV=$($localVars['NODE_ENV'])"
            $out += "PORT=$($localVars['WEBHOOKS_SERVICE_PORT'])"
            if ($localVars.ContainsKey('APP_VERSION')) { $out += "APP_VERSION=$($localVars['APP_VERSION'])" }
            $out += ""
        }
        "worker" {
            $out += "NODE_ENV=$($localVars['NODE_ENV'])"
            $out += "PORT=$($localVars['WORKER_SERVICE_PORT'])"
            if ($localVars.ContainsKey('APP_VERSION')) { $out += "APP_VERSION=$($localVars['APP_VERSION'])" }
            if (-not $localVars.ContainsKey('ENABLE_AUTONOMOUS_MEMORY_ADAPTER')) { $localVars['ENABLE_AUTONOMOUS_MEMORY_ADAPTER'] = 'true' }
            if (-not $localVars.ContainsKey('ENABLE_MEMORY_CONSOLIDATION')) { $localVars['ENABLE_MEMORY_CONSOLIDATION'] = 'false' }
            if (-not $localVars.ContainsKey('AUTONOMOUS_MEMORY_STORE_MODE')) { $localVars['AUTONOMOUS_MEMORY_STORE_MODE'] = 'ceo_autonomous' }
            if (-not $localVars.ContainsKey('WORKER_API_RPC_TIMEOUT_MS')) { $localVars['WORKER_API_RPC_TIMEOUT_MS'] = '120000' }
            if (-not $localVars.ContainsKey('WORKER_ACTOR_USER_ID')) { $localVars['WORKER_ACTOR_USER_ID'] = '00000000-0000-4000-8000-000000000001' }
            $out += ""
        }
        "logging" {
            $out += "NODE_ENV=$($localVars['NODE_ENV'])"
            $out += "PORT=$($localVars['LOGGING_SERVICE_PORT'])"
            if ($localVars.ContainsKey('HOSTNAME')) { $out += "HOSTNAME=$($localVars['HOSTNAME'])" }
            $out += ""
        }
    }

    $added = @{}
    foreach ($var in $requiredVars) {
        if ($localVars.ContainsKey($var)) {
            $out += "$var=$($localVars[$var])"
            $added[$var] = $true
        }
    }

    Ensure-Directory -filePath $outputPath
    $out -join "`n" | Out-File -FilePath $outputPath -Encoding utf8 -NoNewline
    Write-Host "Generated: $outputPath" -ForegroundColor Green
}

function Generate-DockerEnvFile {
    param(
        [hashtable]$variables,
        [string]$outputPath
    )

    $dockerVars = @{}
    foreach ($k in $variables.Keys) {
        $dockerVars[$k] = $variables[$k]
    }

    $dockerVars['DB_HOST'] = 'postgres'
    $dockerVars['POSTGRES_HOST'] = 'postgres'
    $dockerVars['REDIS_HOST'] = 'redis'
    $dockerVars['RABBITMQ_HOST'] = 'rabbitmq'
    $dockerVars['RABBITMQ_URI'] = "amqp://$($dockerVars['RABBITMQ_USER']):$($dockerVars['RABBITMQ_PASSWORD'])@rabbitmq:$($dockerVars['RABBITMQ_PORT'])/"
    $dockerVars['RMQ_URL'] = $dockerVars['RABBITMQ_URI']
    # Hybrid topology default:
    # - api/gateway run on host
    # - other services run in docker
    # Docker containers should call host api via host.docker.internal.
    $dockerVars['API_SERVICE_URL'] = 'http://host.docker.internal:3000'
    $dockerVars['WEBHOOKS_SERVICE_URL'] = 'http://webhooks-service:3003'
    $dockerVars['WORKER_SERVICE_URL'] = 'http://worker-service:3004'
    $dockerVars['LOGGING_SERVICE_URL'] = 'http://logging-service:3001'
    $dockerVars['LOKI_URL'] = 'http://loki:3100'
    if ($dockerVars.ContainsKey('CONSUL_HOST')) { $dockerVars['CONSUL_HOST'] = 'consul' }

    $out = @()
    $out += "# docker env"
    $out += "# generated by scripts/env-manager.ps1"
    $out += "# generated at: $(Get-Date -Format 'yyyy-MM-dd HH:mm:ss')"
    $out += ""
    foreach ($k in ($dockerVars.Keys | Sort-Object)) {
        $out += "$k=$($dockerVars[$k])"
    }

    Ensure-Directory -filePath $outputPath
    $out -join "`n" | Out-File -FilePath $outputPath -Encoding utf8 -NoNewline
    Write-Host "Generated: $outputPath" -ForegroundColor Green
}

function Generate-PostgresEnvFile {
    param(
        [hashtable]$variables,
        [string]$outputPath
    )

    $pgVars = @{}
    foreach ($k in $variables.Keys) {
        $pgVars[$k] = $variables[$k]
    }

    # Force IPv4 loopback for local migration and CLI connections.
    $pgVars['DB_HOST'] = '127.0.0.1'
    $pgVars['POSTGRES_HOST'] = '127.0.0.1'

    $keys = @(
        "POSTGRES_USER","POSTGRES_PASSWORD","POSTGRES_DB","POSTGRES_PORT","POSTGRES_HOST",
        "DB_HOST","DB_PORT","DB_USERNAME","DB_PASSWORD","DB_DATABASE"
    )

    $out = @()
    $out += "# PostgreSQL Environment Variables"
    $out += "# generated by scripts/env-manager.ps1"
    $out += "# generated at: $(Get-Date -Format 'yyyy-MM-dd HH:mm:ss')"
    $out += ""
    foreach ($k in $keys) {
        if ($pgVars.ContainsKey($k)) {
            $out += "$k=$($pgVars[$k])"
        }
    }

    Ensure-Directory -filePath $outputPath
    $out -join "`n" | Out-File -FilePath $outputPath -Encoding utf8 -NoNewline
    Write-Host "Generated: $outputPath" -ForegroundColor Green
}

